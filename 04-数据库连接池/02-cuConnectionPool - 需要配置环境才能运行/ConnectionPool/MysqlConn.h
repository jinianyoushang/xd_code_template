#pragma once
#include <iostream>
#include <mysql.h>
#include <chrono>
using namespace std;
using namespace chrono;
class MysqlConn
{
public:
    // 初始化数据库连接
    MysqlConn();
    // 释放数据库连接
    ~MysqlConn();
    // 连接数据库
    bool connect(const string& user, const string& passwd, const string& dbName, const string& ip, unsigned short port = 3306) const;
    // 更新数据库: insert, update, delete
    bool update(const string& sql) const;
    // 查询数据库
    bool query(const string& sql);
    // 遍历查询得到的结果集
    bool next();
    // 得到结果集中的字段值
    string value(int index) const;
    // 事务操作
    bool transaction() const;
    // 提交事务
    bool commit() const;
    // 事务回滚 
    bool rollback() const;
    // 刷新起始的空闲时间点
    void refreshAliveTime();
    // 计算连接存活的总时长
    long long getAliveTime() const;
private:
    void freeResult();

public:

private:
    MYSQL* m_conn = nullptr;
    MYSQL_RES* m_result = nullptr;
    MYSQL_ROW m_row = nullptr;
    steady_clock::time_point m_alivetime;
};

